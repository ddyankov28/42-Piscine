int	ft_str_is_numeric(char *str)

{
	int	a;

	a = 0;
	while ((str[a] >= '0') && (str[a] <= '9'))
	{
		a++;
	}
	if (str[a] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
