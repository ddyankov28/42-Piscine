int	ft_str_is_alpha(char *str)
{
	int	a;

	a = 0;
	while ((str[a] >= 'a' || str[a] >= 'A') && (str[a] <= 'z' || str[a] <= 'Z'))
	{
		a++;
	}
	if (str[a] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
