int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	a;

	a = 0;
	while (s1[a] == s2[a] && s1[a] != '\0' && s2[a] != '\0' && a < n)
	{
		a++;
	}
	if (a == n)
		return (0);
	return (s1[a] - s2[a]);
}
